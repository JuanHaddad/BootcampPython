def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {
    "+": add,
    "-": subtract,
    "*": multiply,
    "/": divide
}

def calculator():
    num1 = float(input("What's the first number? "))
    for symbol in operations:
        print(symbol, end="  ")
    print()
    response = True
    while response:
        operation_symbol = input("Pick an operation from the line above: ")
        num2 = float(input("What's the second number? "))
        calculation_function = operations[operation_symbol]
        answer = calculation_function(num1, num2)
        print(f'{num1} {operation_symbol} {num2} = {answer}')
        resposta = input(f"Type 'y' to continue calculating with {answer}, or type 'o' to a new calculate. and 'e': ")
        if resposta == 'y':
            num1 = answer
        elif resposta == 'o':
            response = False
            clear()
            calculator()
        elif resposta == "e":
            response = False

calculator()